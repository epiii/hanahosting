<h2>Cara Berdonasi</h2>

<div class="alert alert-info">
<legend>Jemput Donasi GRATIS</legend>

<p align="justify">Kemudahan anda dalam berdonasi merupakan kebahagiaan kami dalam melayani sobat Yatim semua. Tunaikan  zakat, infaq, shadaqah dengan mudah. Tinggal hubungi Kantor Layanan terdekat, ZIS Consultant siap membantu Anda dalam pendampingan, konsultasi, dan penjemputan donasi.
</p>
<legend>Transfer Donasi Via ATM</legend>
<p align="justify">Kini Anda dapat dengan mudah menunaikan donasi, cepat, aman melalui kartu debet Anda di jutaan merchant di seluruh dunia, puluhan ribu merchant dan Ribuan ATM (jaringan ATM BCA, ALTO, dan ATM Bersama) serta 900 ribu ATM (Visa/Plus) di seluruh Indonesia.
</p>
<legend>e-Banking</legend>
<p align="justify">Silahkan kunjungi website http://yatimmandiri.org/rekening-donasi dari gadget/handphone Anda, pilih link e-banking Anda, lakukan transfer secara online melalui e-banking bank manapun dngan mudah cepat dan aman.
</p>
<legend>Mobile Banking</legend>
<p align="justify">Klik ke http://yatimmandiri.org/rekening-donasi dari gadget/handphone Anda, pilih link e-banking Anda, lakukan transfer secara online melalui Mobile Banking gadget/handphone anda dengan mudah cepat dan aman.
</p>
<legend>Donasi Via Kartu Kredit</legend>
<p align="justify">Jika anda tidak ada cukup waktu untuk melakukan donasi, manfaatkan Kartu Kredit anda dengan cara mengirimkan SMS maupun email, yang berformat : Nomor Kartu Anda#Batas Masa Berlaku#Jenis Program#Jumlah Donasi kirim ke SMS centre di nomor 0856 4844 3000 atau kirim email ke 'donation@yatimmandiri.org' (tanpa tanda petik (') Selanjutnya petugas Officer kami akan mem-follow up untuk memastikan data Anda.
</p>
<legend>Counter Donasi Yatim Mandiri</legend>
<p align="justify">Saat ini anda juga bisa berdonasi sambil berbelanja. Silakan kunjungi Counter Center kami yang terdekat dengan Anda.
</p>
